// O operador de adição ele soma mas também ele concatena. Tome cuidado
console.log(1 + 1); // Adição
console.log(25 + 50)
console.log("Alex " + "Ventura");
console.log("Nome: " + "Alex Ventura " + ", " + "Idade: " + (24 + 1));


// Subtração
console.log(1 - 1);
console.log("1" - 1);

// Multiplicação e Divisão
console.log(2 * 10);
console.log(10 * 5);

console.log(50 / 10);
console.log(20 / 10);

// Remainder
// Ele sempre traz o resto da divisão
console.log(4 % 2);
console.log(4 % 3);

// Atribuição
/*
    let, var -- pode ser alterado
    const - não pode ser alterado

    =, +=, -=, *=, /=
*/
var a = 6;
var b = 2;

a += 1; // Atribuição de mais um
a -= 1; // Retirando menos um
a *= 2; // Multiplicando mais dois
a /= 3; // Dividindo

console.log(a);
console.log(a + b);


